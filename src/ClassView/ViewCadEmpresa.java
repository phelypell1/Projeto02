    public void ReadTable() {
        DefaultTableModel table = (DefaultTableModel) jTableEmpresas.getModel();
        tbl_EmpresaCad cad = new tbl_EmpresaCad();
        table.setNumRows(0);
        for (tbl_EmpresaBeans emp : cad.TableEmpresas()) {
            table.addRow(new Object[]{
                emp.getId_emp(),
                emp.getNome_emp(),
                emp.getEnd_emp(),
                emp.getCid_emp(),
                emp.getEst_emp(),
                emp.getCep(),
                emp.getAtiva(),
                emp.getNum_tel(),});
        }
    }

