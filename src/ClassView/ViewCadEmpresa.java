    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        tbl_EmpresaBeans emp = new tbl_EmpresaBeans();
        tbl_EmpresaCad cad = new tbl_EmpresaCad();
        if (textEmpresa.getText().equalsIgnoreCase("") || textRua.getText().equalsIgnoreCase("") || textCep.getText().equalsIgnoreCase("")
                || textTelefone.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Existem campos sem preencher. \n"
                    + "Revise os campos e tente nomvamente !");
        } else {
            emp.setNome_emp(textEmpresa.getText());
            emp.setEnd_emp(textRua.getText());
            emp.setCid_emp((tbl_CidadesBeans) jcbCidade.getSelectedItem());
            emp.setEst_emp((tbl_EstadoBeans) jcbEstado.getSelectedItem());
            emp.setCep(textCep.getText());
            if (jCheckBoxTrue.isSelected()) {
                emp.setAtiva(jCheckBoxTrue.getActionCommand());
            } else {
                emp.setAtiva(jCheckBoxfalse.getActionCommand());
            }
            emp.setNum_tel(textTelefone.getText());
            cad.CadastatroEmp(emp);
            
            ReadTable();
        }
            textEmpresa.setText("");
            textRua.setText("");
            String atv = "vazio";
            if(atv.equals("vazio")){
                jCheckBoxTrue.setSelected(false);
                jCheckBoxfalse.setSelected(false);
            }
            
            textCep.setText("");
            textTelefone.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void jTableEmpresasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEmpresasMouseClicked
        DefaultTableModel model = (DefaultTableModel) jTableEmpresas.getModel();
        int i = jTableEmpresas.getSelectedRow();
        if (jTableEmpresas.getSelectedRow() != -1) {
            textEmpresa.setText(jTableEmpresas.getValueAt(jTableEmpresas.getSelectedRow(), 1).toString());
            textRua.setText(jTableEmpresas.getValueAt(jTableEmpresas.getSelectedRow(), 2).toString());

            Object cidade = (jTableEmpresas.getValueAt(jTableEmpresas.getSelectedRow(), 3).toString());
            String cid = cidade.toString();
            jcbCidade.setSelectedItem(cid);

            Object estado = (jTableEmpresas.getValueAt(jTableEmpresas.getSelectedRow(), 4).toString());
            String est = estado.toString();
            jcbEstado.setSelectedItem(est);

            textCep.setText((String) jTableEmpresas.getValueAt(jTableEmpresas.getSelectedRow(), 5));
            String atv = model.getValueAt(i, 6).toString();
            if (atv.equals("True")) {
                jCheckBoxTrue.setSelected(true);
                jCheckBoxfalse.setSelected(false);
              
            }else{
                jCheckBoxfalse.setSelected(true);
                jCheckBoxTrue.setSelected(false);
            }
            textTelefone.setText(jTableEmpresas.getValueAt(jTableEmpresas.getSelectedRow(), 7).toString());
        }
    }//GEN-LAST:event_jTableEmpresasMouseClicked

